package com.chufang.parameter;

public class YuangongParam {
	private int yuangongid;
	private String shoujihm;
	private String xingming;
	private int gangwei;
	private String quanxian;
	private int jinyongbz;
	
	public int getYuangongid() {
		return yuangongid;
	}
	public void setYuangongid(int id) {
		this.yuangongid = id;
	}
	
	public String getShoujihm() {
		return shoujihm;
	}
	public void setShoujihm(String shoujihm) {
		this.shoujihm = shoujihm;
	}
	public String getXingming() {
		return xingming;
	}
	public void setXingming(String xingming) {
		this.xingming = xingming;
	}
	public int getGangwei() {
		return gangwei;
	}
	public void setGangwei(int gangwei) {
		this.gangwei = gangwei;
	}
	public String getQuanxian() {
		return quanxian;
	}
	public void setQuanxian(String quanxian) {
		this.quanxian = quanxian;
	}
	public int getJinyongbz() {
		return jinyongbz;
	}
	public void setJinyongbz(int jinyongbz) {
		this.jinyongbz = jinyongbz;
	}
}
